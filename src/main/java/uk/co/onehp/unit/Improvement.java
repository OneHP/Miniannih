package uk.co.onehp.unit;

public abstract class Improvement {

	public double applyAttack(double input) {
		return input;
	}

	public double applyDefense(double input) {
		return input;
	}

	public float applySpeed(float input) {
		return input;
	}

	public double applyRepair(double input) {
		return input;
	}

	public double applyRange(double input) {
		return input;
	}

	public double applyFireRate(double input) {
		return input;
	}
}
